package IAStuff;

public class IASolution {
	private String algorithm;
	private long duration;
	private TreeNode solution;
	private Tree searchTree;
	
	
	
	public String getAlgorithm() {
		return algorithm;
	}
	public void setAlgorithm(String algorithm) {
		this.algorithm = algorithm;
	}
	public long getDuration() {
		return duration;
	}
	public void setDuration(long duration) {
		this.duration = duration;
	}
	public TreeNode getSolution() {
		return solution;
	}
	public void setSolution(TreeNode solution) {
		this.solution = solution;
	}
	public Tree getSearchTree() {
		return searchTree;
	}
	public void setSearchTree(Tree seachTree) {
		this.searchTree = seachTree;
	}
	
	
}

